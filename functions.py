def area(width, height):
    result = width * height
    return result

result = area(5, 6)
print(result)

result = area(256, 934)
print(result)

result = area(7952, 5304)
print(result)

def subtract(num1, num2):
    result = num1 - num2
    return result

result = subtract(12, 4)
print(result)

result = subtract(-12, -4)
print(result)